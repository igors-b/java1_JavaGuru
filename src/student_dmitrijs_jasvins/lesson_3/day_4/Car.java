package student_dmitrijs_jasvins.lesson_3.day_4;

public class Car {

    private String model;
    private String owner;

    public Car(String model, String owner) {
        //
        this.model = model;
        this.owner = owner;
    }

    public String getModel() {
        return model;
    }
    public String getOwner(){
        return owner;
    }
}
