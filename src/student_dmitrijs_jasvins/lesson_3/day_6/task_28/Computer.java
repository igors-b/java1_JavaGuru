package student_dmitrijs_jasvins.lesson_3.day_6.task_28;

class Computer {
    private String name;
    private String model;

    public Computer(String name, String model) {
        this.name = name;
        this.model = model;
    }

    public String getName() {
        return name;
    }

    public String getModel() {
        return model;
    }
}
