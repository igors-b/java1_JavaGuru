package student_anvars_intezars.home_tasks.lesson_9.level_1.task4;

class Safe {

    public int pinCode;
    public int money;

    Safe(int pinCode, int money) {
        this.pinCode = pinCode;
        this.money = money;
    }

    int getPinCode() {
        return pinCode;
    }

    int getMoney() {
        return money;
    }

}
